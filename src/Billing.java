    private void addbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addbuttonActionPerformed
        // TODO add your handling code here:
        double price = Double.parseDouble(ratefield.getText());
        double quintity= Double.parseDouble(quilityfield.getText());
        int discount=Integer.parseInt(diiscountfield.getText());
        double totall=(price*quintity)*discount/100;
        DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
   
        model.addRow(new Object[]{productnamefield.getText(),price,quintity,discount,totall});
        finalTotall =finalTotall+totall;
        String finalTotal=String.valueOf(finalTotall);
        totallfield.setText(finalTotal);
    }//GEN-LAST:event_addbuttonActionPerformed

