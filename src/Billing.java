    private void idfieldActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_idfieldActionPerformed
        // TODO add your handling code here:
        String pid = idfield.getText();
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/billingg", "root", "");
            
            Statement st = con.createStatement();
            ResultSet rs;
            rs = st.executeQuery("select * from product_information where  Product_Id like'" + pid + "%'");
            if (rs.next()) {
                idfield.setText(rs.getString(3));
                productnamefield.setText(rs.getString(1));
                ratefield.setText(rs.getString(2));
                diiscountfield.setText(rs.getString(4 ));
                quilityfield.setText("1");
                
            } else {
                productnamefield.setText(rs.getString(""));
                ratefield.setText(rs.getString(""));
                
                quilityfield.setText("");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        

    }//GEN-LAST:event_idfieldActionPerformed

